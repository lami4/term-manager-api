package com.selyuto.termbase.dto;

public class ColumnPositionSetting {
    private int id;
    private String name;
    private int position;

    public ColumnPositionSetting() {
    }

    public ColumnPositionSetting(int id, String name, int position) {
        this.id = id;
        this.name = name;
        this.position = position;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPosition() {
        return position;
    }

    public void setPosition(int position) {
        this.position = position;
    }
}
